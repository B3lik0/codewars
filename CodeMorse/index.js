decodeMorse = function (morseCode) {
    return morseCode
        .split(" ")
        .map((word) => {
            return MORSE_CODE[word] == undefined ? " " : MORSE_CODE[word];
        }).join("").trim().replace(/\s{2,}/g, ' ')
};

